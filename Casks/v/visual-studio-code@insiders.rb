  arch arm: "-arm64"

  version "1.120.0-insider,1778043913939,18a02327e146205cf696f8a5babbd036b6a8f743"
  sha256 arm:   "713e18d229dff1b922c9db3960ce9b7f13f2bf7523df3b02b951700363660401",
